public class C21Loops {
    public static void main(String[] args) {
       //types of loops
       /*while loop 
       do while loop
       for loop*/
       System.out.println("using loops");
       float i=-3;
       while (i<=3) {                      
           System.out.println("hello");     //if the codition is not false is keep executing
           i++;
       } 

       int n=100;
       while (n<=200) {
           System.out.println(n);
           n++;
       }
           
       
    
    }
    
}
